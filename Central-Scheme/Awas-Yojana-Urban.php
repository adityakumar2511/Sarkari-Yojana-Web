							    इस प्रधानमंत्री आवास योजना-शहरी के लाभ निम्नलिखित हैंः
								<ol>
                                    <li>
                                      इस योजना के तहत आवेदक को एक संसाधन के रूप में भूमि का उपयोग करते हुए निजी डेवलपर्स की भागीदारी के साथ पात्र स्लम निवासियों का स्लम पुनर्वास दिया जाता हैं।
                                    </li>
                                    <li>
                                  इस योजना के तहत आवेदक क्रेडिट लिंक्ड सब्सिडी योजना के माध्यम से किफायती आवास को बढ़ावा देना (CLSS). EWS: वार्षिक घरेलू आय Rs.3,00,000 तक; घर का आकार 30 sq.m तक; LIG: वार्षिक घरेलू आय Rs.3,00,001 से Rs.6,00,000 तक; घर का आकार 60 sq.m तक; MIG I: रु। 6,00,001 से रु। 12,00,000; घर का आकार 160 sq.m तक; MIG II: वार्षिक घरेलू आय Rs.12,00,001 और 18,00,000 से; घर का आकार 200 sq.m तक का लाभ प्राप्त होगा।
                                    </li>
                                    <li>
                                    इस योजना के तहत आवेदक सार्वजनिक और निजी क्षेत्रों के साथ साझेदारी में किफायती आवासः उन परियोजनाओं में प्रति ईडब्ल्यूएस घर प्रति केंद्रीय सहायता जहां 35% घर ईडब्ल्यूएस के लिए हैं, लाभार्थी के नेतृत्व वाले व्यक्तिगत घर निर्माण/वृद्धि के लिए सब्सिडीः ईडब्ल्यूएस श्रेणी के व्यक्तियों के लिए जिन्हें व्यक्तिगत घर की आवश्यकता होती है।
                                    </li>
								</ol>
								उपर्युक्त लाभ इस प्रधानमंत्री आवास योजना-शहरी के तहत दिए जाएंगे।
