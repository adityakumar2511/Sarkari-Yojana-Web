								<ul>
									<h5 style="text-align:left;">1. बुनियादी बचत बैंक जमा खाता:</h5>
                                    <li >नियमित बैंक खाता खोलने के लिए योग्य कोई भी भारतीय नागरिक बीएसबीडीए खोल सकता है। इस तरह के खाते में किसी न्यूनतम शेष राशि के रखरखाव की आवश्यकता नहीं होती है। खाताधारक नकदी जमा करने और निकालने के लिए बैंक शाखाओं, एटीएम और बैंकिंग संवाददाताओं (बीसी) दोनों का उपयोग कर सकते हैं। हालांकि, कोई भी व्यक्ति महीने में चार बार से अधिक नकदी नहीं निकाल सकता है। </li>
                                    <br>
                                   <h5 style="text-align:left;">2. छोटा खाता/छोटा खाता:</h5>
                                    <li>जन धन योजना के तहत लोग कानूनी दस्तावेज पेश किए बिना छोटे बैंक खाते खोल सकते हैं। ये खाते आम तौर पर बारह महीने की अवधि के लिए वैध होंगे। इसके बाद, ऐसे खातों को बारह महीने की और अवधि के लिए जारी रखने की अनुमति दी जाएगी, यदि खाताधारक एक दस्तावेज प्रदान करता है जिसमें दिखाया गया है कि उसने छोटा खाता खोलने के 12 महीने के भीतर किसी भी आधिकारिक रूप से वैध दस्तावेज के लिए आवेदन किया है।</li>
                                    <br>
                                   <h5 style="text-align:left;">3. इनबिल्ट दुर्घटना बीमा के साथ रुपे डेबिट कार्ड:</h5>
                                    <li>पीएमजेडीवाई के तहत, सभी लाभार्थियों को Rs.2 लाख (28.08.2018 से पहले खोले गए खातों के लिए 1 लाख रुपये) के इनबिल्ट दुर्घटना बीमा कवर के साथ एक मुफ्त रुपे डेबिट कार्ड जारी किया जाता है।</li>
                                    <br>
                                    <h5 style="text-align:left;">4. ओवरड्राफ्ट सुविधा:</h5>
                                    <li>पीएमजेडीवाई के तहत लाभार्थी 500 रुपये तक की ओडी सुविधा का लाभ उठा सकते हैं। 10, 000।
                                        बिजनेस कॉरेस्पॉन्डेंट्स (बीसी)/बैंक मित्र
                                        बीसी/बैंक मित्र बैंक शाखा/एटीएम के अलावा अन्य स्थानों पर बैंकिंग सेवाएं प्रदान करने के लिए बैंकों द्वारा लगाए गए खुदरा एजेंट हैं। यह मुख्य रूप से ग्रामीण क्षेत्रों के लिए है, जहाँ बैंक की शाखाएँ दूर-दूर तक हैं। बीसी/बैंक मित्र निवासियों से जुड़ते हैं और उन्हें बचत खाते, जमा, भुगतान और निकासी, लघु खाता विवरण आदि जैसे बैंकिंग समाधानों के साथ मार्गदर्शन करते हैं। बीसी/बैंक मित्रों के अपने मजबूत नेटवर्क के साथ बैंक दूरदराज/ग्रामीण क्षेत्रों में ग्राहकों को परेशानी मुक्त सेवाएं प्रदान कर रहे हैं।.</li>
								</ul>
