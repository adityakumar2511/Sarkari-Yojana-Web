                                        पीएमजेडीवाई देश भर में बैंकिंग पहुंच बढ़ाने और वित्तीय समावेशन को बढ़ावा देने में सफल रहा है। इस योजना ने विभिन्न वित्तीय सेवाओं तक पहुंच सुनिश्चित की है जैसे कि बुनियादी बचत बैंक खाते की उपलब्धता, आवश्यकता आधारित क्रेडिट तक पहुंच, प्रेषण सुविधा, बीमा और पेंशन को बहिष्कृत धाराओं यानी i.e. कमजोर वर्ग और कम आय वाले समूह।
