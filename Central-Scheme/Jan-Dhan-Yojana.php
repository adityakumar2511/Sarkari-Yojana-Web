								<ol>
									<li>
                                        प्रधानमंत्री द्वारा अगस्त, 2014 में देश भर में वित्तीय समावेशन के लिए एक राष्ट्रीय मिशन (एनएमएफआई), प्रधानमंत्री जन धन योजना (पीएमजेडीवाई) की शुरुआत की गई थी, ताकि यह सुनिश्चित किया जा सके कि नागरिक अपनी वित्तीय गतिविधियों की परिकल्पना कर सकें। पीएमजेडीवाई बैंकिंग सुविधा से वंचित प्रत्येक परिवार के लिए सार्वभौमिक बैंकिंग सेवाएं प्रदान करता है, जो बैंकिंग सुविधा से वंचित लोगों को सुरक्षित करने, असुरक्षित लोगों को वित्त पोषण करने और सेवा से वंचित और कम सेवा वाले क्षेत्रों में सेवा देने के मार्गदर्शक सिद्धांतों पर आधारित है। सरकार की वित्तीय समावेशन पहलों को बढ़ावा देने के लिए, पीएमजेडीवाई योजना को 14.08.2018 से आगे बढ़ा दिया गया था, जिसमें "हर घर" से "हर बैंक वाले वयस्क" के लिए खाते खोलने पर ध्यान केंद्रित किया गया था। 
