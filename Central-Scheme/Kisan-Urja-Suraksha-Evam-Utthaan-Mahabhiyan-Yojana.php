						    <td colspan="2">
						        <h3 class="tablehead"> Objective of Pradhan Mantri Kisan Urja Suraksha Evam Utthaan Mahabhiyan Yojana</h3>
						    </td>
						</tr>
						<tr>
							<td colspan="2">
                                प्रधानमंत्री किसान ऊर्जा सुरक्षा एवं उत्थान महाभियान योजना के निम्नलिखित उद्देश्य हैं:
								<ol value="1">
									<li>
                                        पीएम कुसुम योजना मुख्य उद्देश्य भारत के हमारे किसानों को अत्याधुनिक तकनीक उपलब्ध कराना है और उन्हें कृषि के क्षेत्र को डीजल रहित सिंचाई के लिए स्रोत प्रदान करने का लक्ष्य रखा गया है।
									</li>
									<li>	
                                        हमारे किसानों को अधिक प्रभावी और पर्यावरण के अनुकूल सिंचाई में सहायता देने के लिए सौर पंप दिया जाता है क्योंकि ये सुरक्षित ऊर्जा उत्पन्न करने में सक्षम हैं। और साथ ही पंप सेट में एक ऊर्जा पावर ग्रिड होता है जो डीजल संचालित पंपों की अपेक्षा में अधिक ऊर्जा उत्पन्न करने में सक्षम है। जिससे किसान अपनी आय बढ़ाने के लिए अतिरिक्त बिजली सीधे हमारी सरकार को बेच सकेंगे और लाभ प्राप्त कर सकेंगे।
									</li>
								</ol>
							</td>
						</tr>
