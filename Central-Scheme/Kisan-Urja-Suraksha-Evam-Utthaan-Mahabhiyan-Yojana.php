						<tr>
							<td colspan="2">
							    प्रधानमंत्री किसान ऊर्जा सुरक्षा एवं उत्थान महाभियान योजना में आवेदन करने के लिए आवेदकों को निम्नलिखित चरणों का पालन करना होगाः
								<ol>
									<li>
										सर्वप्रथम कुसुम योजना की आधिकारिक पोर्टल पर जाएं और रजिस्ट्रेशन ऑप्शन पर क्लिक करें।
									</li>
									<li>
                                        अब इसके बाद आप सभी आवश्यक विवरणों के साथ अपनी स्क्रीन पर दिखाई देने वाले पंजीकरण फॉर्म को ध्यानपूर्वक भरें।
									</li>
									<li>
                                        अब इसके बाद घोषणा बॉक्स की जांच करें और "सबमिट" ऑप्शन पर क्लिक करें। 
									</li>
									<li>
                                        अब इसमें पंजीकरण करने के बाद, आप सौर कृषि पंपसेट सब्सिडी योजना 2023 के लिए "लॉगिन" ऑप्शन पर क्लिक करें।
									</li>
									<li>
                                        अब इसके बाद आप ऑनलाइन आवेदन पत्र में सभी मांगी गयी सम्पूर्ण आवश्यक जानकारी सटीक रूप से प्रदान करें,  चरण 6: अब अंत में सभी सहायक दस्तावेजों को संलग्न करें और जमा करें जिससे आपका आवेदन सफलतापूर्वक़ सम्पूर्ण हों जायेगा। 
									</li>
								</ol>
								अब प्रधानमंत्री किसान ऊर्जा सुरक्षा एवं उत्थान महाभियान योजना में आपका आवेदन सफलतापूर्वक पूरा हो जाएगा।
