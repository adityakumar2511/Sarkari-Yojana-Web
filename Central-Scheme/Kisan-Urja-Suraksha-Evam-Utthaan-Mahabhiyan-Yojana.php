						<tr>
						    <td colspan="2">
						        <h3 class="tablehead">Benefits for Pradhan Mantri Kisan Urja Suraksha Evam Utthaan Mahabhiyan Yojana</h3>
						    </td>
						</tr>
						<tr>
							<td colspan="2">
							    इस प्रधानमंत्री किसान ऊर्जा सुरक्षा एवं उत्थान महाभियान योजना के लाभ निम्नलिखित हैंः
								<ol>
									<li>
									    भारत सरकार ने सौर संयंत्रों का निर्माण शुरुआत की है  जो कुल 28,250 मेगावाट बिजली उत्पन्न कर सकती है। सरकार के द्वारा 60% सब्सिडी देगी और कुल लागत का 30% ऋण प्रदान करेगी। जिसके कारण हमारे किसानों को सोलर प्लांट और सोलर पंप लगाने की कुल लागत का केवल 10% ही वहन करना पड़ेगा।
									</li>
									<li>	
                                        कुसुम योजना के अंतर्गत हमारी सरकार अत्याधुनिक सौर पंप स्थापित करने के लिए सब्सिडी प्रदान करती है। जो सिंचाई में सुधार करते हैं क्योंकि यह 720 मेगावाट क्षमता रखते हैं।
									</li>
									<li>
                                        इस योजना के अंतर्गत हमारे किसानों को संयंत्रों से उत्पन्न अतिरिक्त बिजली सीधे हमारी सरकार को बेचने का अवसर प्रदान मिलता है। जिससे हमारे किसानों की आय में वृद्धि की  सुनिश्चित होती है। 
									</li>
									<li>
										इस योजना के अंतर्गत ग्रामीण क्षेत्र में किसान अपनी एक भूमि 25 वर्षों के लिए सौर संयंत्र कार्यान्वयन के लिए बंजर और बिना खेती वाली भूमि का उपयोग करके आय का एक स्थिर स्रोत प्राप्त कर सकता है जिससे उसको लाभ प्राप्त होता है। 
									</li>
									<li>
                                        इस योजना के अंतर्गत सौर संयंत्रों को खेती योग्य भूमि में न्यूनतम ऊंचाई से ऊपर स्थापित किया जाता है। जिससे हमारे किसान पौधे लगाने के बाद भी खेती जारी रख सकते हैं। 
									</li>
									<li>
                                        इस योजना के अंतर्गत किसान यह सुनिश्चित कर सकते हैं कि  ऊर्जा का बढ़ता उपयोग खेतों में प्रदूषण को कम करने में मदद करता है और साथ ही पर्यावरण के अनुकूल खेती के लिए एक प्रवेश द्वार खोलता है जिससे उन्हें लाभ मिलता है। 
									</li>
                                    <li>
                                        कुसुम योजना के लिए एक सफल ऑनलाइन आवेदन के बाद ही किसानों को विभाग के द्वारा भेजे गए आपूर्तिकर्ता को सौर पंप स्थापित करने के लिए कुल लागत का 10% जमा करना होगा।सब्सिडी राशि स्वीकृत होने के बाद सौर पंप सेट को सशक्त किया जाता है, जिसमें आम तौर पर 90 से 10 दिन लग सकते हैं। 
                                    </li>
								</ol>
								उपर्युक्त लाभ इस प्रधानमंत्री किसान ऊर्जा सुरक्षा एवं उत्थान महाभियान योजना के तहत दिए जाएंगे।
							</td>
						</tr>
