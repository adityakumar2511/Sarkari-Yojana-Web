                                    </li>
                                    <li>
                                    यदि खेत किसान के स्वामित्व में है, तो <b>'खसरा' कागज और खाता संख्या </b>को एक साथ रखना होगा 
                                    </li> 
									<li>
                                        यदि फसल केवल खेत में बोई गई है तो उसका साक्ष्य के रूप में प्रस्तुत करना होगा।
                                    </li>         
                                    <li>
                                        सबूत के तौर पर किसानों को प्रधान, सरपंच, गोवा प्रधान, पटवारी आदि लोगों से एक पत्र लिखवाना आवश्यक है।
                                    </li> 
								</ol>
								इन सभी दस्तावेजों के साथ आप इस प्रधानमंत्री फसल  बीमा योजना में सफलतापूर्वक अपना आवेदन कर सकते हैं।
