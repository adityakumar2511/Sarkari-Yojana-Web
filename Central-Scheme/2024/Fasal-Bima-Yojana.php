						<tr>
							<td colspan="2">
                                यदि आप प्रधानमंत्री फसल बीमा योजना के लिए अपना ऑनलाइन पंजीकरण कराना चाहते हैं तो आपको इन सभी चरणों का पालन करना होगा: 
								<ol>
									<li>
										सबसे पहले प्रधानमंत्री फसल बीमा योजना की आधिकारिक वेबसाइट <a href="https://pmfby.gov.in/" target="_blank">https://pmfby.gov.in/</a> पर जाना होगा । 
									</li>
                                    <li>
										अब आपको होम पेज पर फार्मर कॉर्नर सेक्शन दिखाई देगा, उस पर क्लिक करें और खुद फसल बीमा के लिए आवेदन पर क्लिक करें।
                                    </li>
                                    <li>
										अब आपको किसान आवेदन पृष्ठ पर निर्देशित किया जाएगा जहां आपको अतिथि किसान विकल्प पर क्लिक करना होगा। 
                                    </li>
                                    <li>
										अब एक रजिस्ट्रेशन फॉर्म खुलेगा. अब आपको किसान विवरण, आवासीय विवरण, किसान आईडी और खाता विवरण सहित सभी आवश्यक जानकारी भरनी ध्यान पूर्वक भरनी होगी। 
                                    </li>
                                    <li>
										अब फॉर्म के नीचे दिए गए कैप्चा कोड को दर्ज करना न भूलें और अब अंत में, आवेदन प्रक्रिया को पूरा करने के लिए सबमिट विकल्प पर क्लिक करें।
                                    </li>
                                    <li>
										इन सभी चरणों का पालन करके आप आसानी से प्रधानमंत्री फसल बीमा योजना के लिए ऑनलाइन पंजीकरण कर सकते हैं और इसका योजना का लाभ प्राप्त कर सकते हैं। 
                                    </li>
								</ol>
							</td>
