							    प्रधानमंत्री फसल  बीमा  योजना के लिए उद्देश्य निम्नलिखित हैं:
								<ol>
									<li>
										प्राकृतिक घटनाओं के कारण फसल हानि या क्षति से प्रभावित होने पर किसानों को आर्थिक सहायता प्रदान करना है।
                                    </li>
                                    <li>
										यह किसानों की आय को भी स्थिरता प्रदान करता है ताकि यह सुनिश्चित हो सके कि वे खेती जारी रख सकें और अधिक से अधिक फसलें पैदा कर सकें और स्थिति सुधार सकें।
                                    </li>
                                    <li>
                                        इससे किसानों को सहारा भी मिलता है और फसल प्रभावित होने पर भी किसानों को बहुत सहायता मिलती है, योजना की वजह से नए किसान भी खेती क्षेत्र से जुड़ रहे हैं।
                                    </li>
								</ol>
							</td>
						</tr>
