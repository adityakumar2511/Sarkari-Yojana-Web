                <br>
                यह योजना मुख्य रूप से प्राकृतिक आपदाओं, कीटों या बीमारियों से फसल के नुकसान की स्थिति में किसानों को आर्थिक सहायता के साथ-साथ बीमा सुरक्षा प्रदान करने के लिए शुरू की गई है। 
            </font>
