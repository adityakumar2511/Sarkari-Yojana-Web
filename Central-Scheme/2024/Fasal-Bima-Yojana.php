			    नई दिल्ली में प्रधानमंत्री फसल बीमा योजना कृषि और किसान कल्याण मंत्रालय द्वारा खरीफ 2016 सीज़न से शुरूआत की गई है। प्रधानमंत्री फसल बीमा योजना किसानों को सहायता दिलाने के लिए है। 
