						<tr>
							<td colspan="2">
							    प्रधानमंत्री सूर्योदय योजना के लिए निम्नलिखित संक्षिप्त जानकारी हैं:
								<ol>
									<li>
										वर्ष 2024 में 22 जनवरी के दिन श्री राम जी के मंदिर के प्राण प्रतिष्ठा का कार्यक्रम चल रहा था। इसी कार्यक्रम को समाप्त करने के बाद पीएम मोदी जी ज़ब वापस लौटे, तो उन्होंने देशवासियों को एक महत्वपूर्ण सौगात दी और एक सोलर योजना को शुरू करने की घोषणा की। 
                                    </li>
                                    <li>
										इस योजना का नाम पीएम मोदी जी के द्वारा सूर्योदय योजना रखा गया है। इस योजना के बारे में जानकारी देते हुए पीएम मोदी जी के द्वारा कहा गया कि सूर्योदय योजना के माध्यम से सभी लाभार्थियों के घरों की छत पर सोलर सिस्टम लगवाया जाएगा, जिससे देश में सोलर ऊर्जा को बढ़ावा मिलेगा। इस प्रयास से लोगों के बिजली के खर्चे में भी बहुत ज्यादा कमी आएगी और लोगों की बिजली पर निर्भरता भी कम हो जायेगी।  देश में अधिकतर समय तेज धूप रहती है इसीलिए यैसी स्थिति में भारत जैसे देश को तेज धूप का लाभ अवश्य ही उठाया जाना चाहिए।
                                    </li>
                                    <li>
                                        भारत देश में साल में कम से कम 5 से लेकर 7 महीने बहुत धूप रहती है। इसी आईडिया को ध्यान में रखते हुए सरकार द्वारा लगातार यह प्रयास कर रही है कि लोगों को सब्सिडी पर या फिर निशुल्क सोलर सिस्टम दिया जाए जिससे जो लोग बिजली का बिल नहीं भर पाते हैं वह सभी लोग भी सोलर एनर्जी का इस्तेमाल करके बिजली की आपूर्ति को पूरा कर सकें। इस योजना की प्रमुख बात यह है की ग्रामीण इलाके में बड़े पैमाने पर चलाने का काम सरकार के द्वारा किया जायेगा, क्योंकि ग्रामीण क्षेत्रों में बहुत ज्यादा गरीबी होती है और ऐसे में यदि लोगों को सोलर एनर्जी का लाभ मिलना शुरू हो जायेगा तो उनकी बिजली पर निर्भरता भी कम होगी, साथ ही बिजली कटौती की वजह से ग्रामीण इलाके के लोगों को गर्मी में जो भीषण गर्मी का सामना करना पड़ता है उससे भी छुटकारा मिल जाएगा।
                                    </li>
