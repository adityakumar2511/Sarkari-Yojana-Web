							    इस प्रधानमंत्री सूर्योदय योजना के लाभ निम्नलिखित हैंः
								<ol>
                                    <li>
                                        इस योजना की शुरुआत करने की घोषणा प्रधानमंत्री मोदी जी के द्वारा की गई जब वह राम मंदिर के कार्यक्रम को समाप्त करके वापस लौट रहे थे।
                                    </li>
                                    <li>
                                        इस योजना को बहुत ही जल्द साल 2024 में अप्रैल या मई के महीने में शुरू किया जायेगा।
                                    </li>
                                    <li>
                                        सरकार के द्वारा बताया गया है की देश में लगभग 1 करोड़ घरों में इस योजना के माध्यम से रूफटॉप सोलर लगवाया जायेगा।
                                    </li>
                                    <li>
                                        प्रधानमंत्री मोदी जी के द्वारा बताया गया है कि सूर्यवंशी  योजना भगवान श्री राम के प्राण प्रतिष्ठा से दुनिया के सभी भक्त हमेशा ऊर्जा हासिल करते रहेंगे। इसलिए आज इस शुभ अवसर पर यह संकल्प मैंने किया है कि देश में लोगों के घरों की छत पर उनका अपना सोलर रुफटॉप सिस्टम लगवाया जायेगा।
                                    </li>
                                    <li>
                                        सूर्योदय योजना के शुरू होने से गरीब और मध्यम वर्ग के बिजली बिल में काफी ज्यादा कमी आएगी और साथ ही हमारा भारत देश सोलर एनर्जी के क्षेत्र में आत्मनिर्भर बनने के लिए अग्रसर होगा और सशक्त बनेगा।
                                    </li>
								</ol>
								उपर्युक्त लाभ इस प्रधानमंत्री सूर्योदय योजना के तहत दिए जाएंगे।
							</td>
