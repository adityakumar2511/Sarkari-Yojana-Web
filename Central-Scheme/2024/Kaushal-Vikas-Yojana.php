						        <h3 class="tablehead">What is Pradhan Mantri Kaushal Vikas Yojana</h3>
						    </td>
						</tr>
						<tr>
							<td colspan="2">
							   प्रधानमंत्री कौशल विकास  योजना में आवेदन करने के लिए आवेदकों को निम्नलिखित चरणों का पालन करना होगाः
								<ol>
									<li>
										PMKVY 2.0 से सीख लेते हुए, भारत की केंद्र सरकार ने PMKVY 3.0 के लॉन्च की घोषणा की। PMKVY 1.0, PMKVY 2.0 से सीखे गए सबक पर आधारित है। इरादा इस योजना को नीतिगत बदलावों और विभिन्न उद्योगों में प्राथमिकताओं में बदलाव के वर्तमान संदर्भ के साथ तालमेल बिठाने का है। योजना का कार्यान्वयन निम्नलिखित दो चरणों में है: 
                                        <ul>
                                            <li>
												चरण 1 - पीएमकेवीवाई 3.0, 2020-21 में पायलट आधार पर तैनात किया जाएगा। (2020-21)। 
                                            </li>
                                            <li>
												चरण 2 - इस योजना के दूसरे चरण (2021-2026) के लिए एक कार्यान्वयन ढांचा विकसित करना शुरू किया जायेगा। 
                                            </li>
                                        </ul>
									</li>
                                    <li>
										पीएमकेवीवाई 3.0 (2020-21) ने अपना ध्यान आपूर्ति-आधारित से मांग-आधारित दृष्टिकोण पर केंद्रित कर दिया है। कोविड-19 के कारण उत्पन्न व्यवधान और इसके परिणामस्वरूप आजीविका पर प्रभाव के बाद, योजना (पीएमकेवीवाई 3.0) अपस्किलिंग/रीस्किलिंग पर जोर देगी। भविष्य के कौशल (उद्योग 4.0) पर ध्यान देने के साथ, यह योजना मौजूदा कार्यबल उत्पादकता को बढ़ावा देने के लिए पाठ्यक्रम प्रदान करती है और व्यापक दर्शकों तक पहुंचने के लिए ऑनलाइन/डिजिटल प्रशिक्षण प्रदान करती है।
                                    </li>
								</ol>
							</td>
						</tr>
						<tr>
						    <td colspan="2">
						        <h3 class="tablehead">Document for Pradhan Mantri Kaushal Vikas Yojana</h3>
						    </td>
						</tr>
						<tr>
							<td colspan="2">
							   प्रधानमंत्री कौशल विकास  योजना के लिए निम्नलिखित दस्तावेज आवश्यक हैंः
								<ol>
									<li>
                                        आवेदन करने के लिए, आवेदक के पास <b> आधार कार्ड या पैन कार्ड या वोटर आईडी कार्ड </b> होना आवश्यक है।
                                    </li>
									<li>
                                       आवेदन करने के लिए, आवेदक के पास <b>बैंक पासबुक</b> होना आवश्यक है।
                                    </li>
                                    
                                    <li>
                                        आवेदन करने के लिए, आवेदक के पास <b> शैक्षणिक योग्यता प्रमाण पत्र (हाईस्कूल / इन्टरमीडिएट / ग्रेजुवेशन)</b> होना आवश्यक है।  
                                    </li> 
								</ol>
								इन सभी दस्तावेजों के साथ आप इस प्रधानमंत्री कौशल विकास  योजना में सफलतापूर्वक अपना आवेदन कर सकते हैं।
							</td>
						</tr>
						<tr>
						    <td colspan="2">
						        <h3 class="tablehead">Eligible Person for Pradhan Mantri Kaushal Vikas Yojana </h3>
						    </td>
						</tr>
						<tr>
							<td colspan="2">
							     प्रधानमंत्री कौशल विकास  योजना के लिए निम्नलिखित पात्रता की आवश्यकता हैः
								<ol>
									<li>
                                        आवेदक एक युवा व्यक्ति है जो बेरोजगार है या उसने स्कूल या कॉलेज छोड़ दिया है।
                                     </li>
                                    <li>
                                        आवेदक के पास आधार कार्ड के साथ-साथ एक सक्रिय बैंक खाता भी होना चाहिए।
                                     </li>
                                    <li>
										पहचान प्रमाण के रूप में आवेदक को पैन या मतदाता पहचान पत्र जैसे पहचान दस्तावेज प्रदान करने होंगे । ये पहचान प्रमाण केवल पूर्वोत्तर क्षेत्र के राज्यों के लिए लागू हैं। (जम्मू-कश्मीर के निवासियों को अतिरिक्त आईडी प्रदान करने की आवश्यकता समय-समय पर जोड़ी जा सकती है)
                                    </li>
                                    <li>
										विभिन्न नौकरी पदों के लिए एसएससी द्वारा स्थापित अतिरिक्त आवश्यकताओं को पूरा करें
										क्योंकि पीएमकेवीवाई स्कूल/कॉलेज छोड़ने वालों पर ध्यान केंद्रित करती है, इसलिए यह योजना कॉलेज के छात्रों को नामांकन की अनुमति नहीं देती है।
                                    </li>
								</ol>
                                इन सभी पात्रताओं के साथ आप इस प्रधानमंत्री कौशल विकास  योजना में सफलतापूर्वक अपना आवेदन कर सकते हैं।							
							</td>
						</tr>
						<tr>
						    <td colspan="2">
						        <h3 class="tablehead">Component of Pradhan Mantri Kaushal Vikas Yojana</h3>
						    </td>
						</tr>
						<tr>
							<td colspan="2">
							    प्रधानमंत्री कौशल विकास  योजना के लिए निम्नलिखित संक्षिप्त जानकारी हैं:
								<ol>
									<li>
										अल्पावधि प्रशिक्षण पीएमकेवीवाई प्रशिक्षण केंद्रों (टीसी) में प्रदान किया जा रहा है। इसका उद्देश्य उन उम्मीदवारों को लाभ पहुंचाना है जो स्कूल/कॉलेज छोड़ चुके हैं या अभी तक बेरोजगार हैं। टीसी राष्ट्रीय कौशल योग्यता फ्रेमवर्क के अलावा सॉफ्ट स्किल, उद्यमिता, वित्तीय साक्षरता और डिजिटल साक्षरता में प्रशिक्षण प्रदान कराना हैं। मूल्यांकन उत्तीर्ण करने के बाद उम्मीदवारों को प्रशिक्षण प्रदाताओं से प्लेसमेंट सहायता प्राप्त कराई जाती है।
                                    </li>
                                    <li>
										पीएमकेवीवाई का विशेष परियोजना घटक एक ऐसे मंच के निर्माण की कल्पना करता कर रहा है जो सरकारी एजेंसियों, निगमों और उद्योग संगठनों के विशेष क्षेत्रों और/या परिसरों में प्रशिक्षण की सुविधा प्रदान करता है। इन विशेष परियोजनाओं में उपलब्ध योग्यता पैक्स /राष्ट्रीय व्यावसायिक मानकों द्वारा परिभाषित नहीं की गई विशेष नौकरी भूमिकाओं में प्रशिक्षण भी शामिल की है। प्रत्येक हितधारक के लिए, विशेष परियोजनाओं के लिए पीएमकेवीवाई की अल्पकालिक प्रशिक्षण आवश्यकताओं में कुछ संशोधन की आवश्यकता होती है।
                                    </li>
								</ol>
							</td>
						</tr>
						<tr>
						    <td colspan="2">
						        <h3 class="tablehead">Features of Pradhan Mantri Kaushal Vikas Yojana</h3>
						    </td>
						</tr>
                        <tr>
							<td colspan="2">
								<ol>
                                    <li>
                                        कौशल प्रशिक्षण में भाग लेने के लिए बड़ी संख्या में भारतीय किशोरों को सक्षम और संगठित करना है जिससे यह सुनिश्चित होगा कि वे काम पा सकें और जीविकोपार्जन कर सकें। 
                                    </li>
                                    <li>
										इसके माध्यम से मौजूदा कार्यबल की उत्पादकता बढ़ाएं और प्रशिक्षण और प्रमाणन को देश की जरूरतों से जोड़ें हैं।
                                    </li>
                                    <li>
										मान्यता प्राप्त संस्थानों से कौशल प्रशिक्षण प्राप्त करने वाले अभ्यर्थियों को पुरस्कृत किया जाना है। 
                                    </li>
									<li>
										प्रत्येक उम्मीदवार को औसतन 8,000 रुपये (आठ हजार रुपये) का इनाम दिया जाएगा।
                                    </li>
                                    <li>
										लगभग रु. की लागत पर. 1,500 करोड़ रुपये से 24 लाख युवाओं की मदद करना लक्ष्य है। 
                                    </li>
								</ol>
							</td>
						</tr>
