							<td colspan="2">
							   प्रधानमंत्री कौशल विकास  योजना में आवेदन करने के लिए आवेदकों को निम्नलिखित चरणों का पालन करना होगाः
								<ol>
									<li>
										आप पीएमकेवीवाई योजना के लिए ऑनलाइन या ऑफलाइन दोनों तरह से आवेदन कर सकते हैं। आप सबसे पहले आधिकारिक वेबसाइट पर जाकर ऑनलाइन पंजीकरण कर सकते हैं। वैकल्पिक रूप से, आप निकटतम प्रधान मंत्री कौशल विकास योजना केंद्रों या केंद्रों पर जाकर पंजीकरण कर सकते हैं। केंद्र पर जाते समय आपको अपना पहचान प्रमाण जैसे आधार, पैन या मतदाता पहचान पत्र साथ रखना होगा।
									</li>
                                    <li>
                                        आपको बैंक विवरण और किसी मौजूदा कौशल का प्रमाण भी देना होगा। 
                                    </li>
                                    <li>
										आपके आवेदन के विस्तृत मूल्यांकन के बाद, प्रशिक्षण केंद्र आपको प्रशिक्षण कार्यक्रम के साथ सबसे उपयुक्त प्रशिक्षण प्रदान करेगा। प्रशिक्षण पूरा करने पर आपको पूर्णता का प्रमाण पत्र प्राप्त होगा। 
                                    </li>
								</ol>
							</td>
