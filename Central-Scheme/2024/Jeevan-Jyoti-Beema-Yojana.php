										बीमाधारक के 55 वर्ष की आयु तक पहुंचने तक सालाना कवरेज की अनुमति दी जायेगी ।
                                    </li>
                                    <li>
										आवेदक के पास एक वित्तीय बचत बैंक खाता होना आवश्यक है।
                                    </li>
									<li>
										आवेदक को योजना के लिए दर का भुगतान करने के लिए व्यक्ति को अपने बचत बैंक खाते से ऑटो-डेबिट करने के लिए बैंक को सहमति देनी होगी ।
                                    </li>
                                    <li>
