						    <td colspan="2">
						        <h3 class="tablehead">Benefits of Pradhan Mantri Jeevan Jyoti Beema Yojana</h3>
						    </td>
						</tr>
						<tr>
							<td colspan="2">
							    प्रधानमंत्री जीवन ज्योति बीमा  योजना के लिए निम्नलिखित संक्षिप्त जानकारी हैं:
								<ol>
									<li>
										भारत के सभी निवासियों के लिए कुछ स्थायी और पूरे वर्ष के सुधार सुनिश्चित करने के लिए, केंद्र सरकार ने प्रधान मंत्री जीवन ज्योति बीमा योजना 2024 शुरूआत की है। एक बार जब आप प्रधान मंत्री जीवन ज्योति बीमा योजना के बारे में जान लेंगे तब इसका लाभ उठाया सकेंगे।
                                    </li>
                                    <li>
										इस योजना के अंतर्गत आप सभी लाभार्थियों को कई लाभ मिलेंगे जैसे की प्रीमियम राशि जमा करने पर लाभार्थियों को 2 लाख रुपये का जोखिम कवरेज प्रदान किया जा रहा है।
                                    </li>
                                    <li>
                                        यह योजना लोगों के सतत सुधार आर्थिक स्थिति में सुधार को सुनिश्चित करती है।
                                    </li>
									<li>
										इस योजना का प्रथम उद्देश्य देश के लोगों का विकास करना है।
									</li>
								</ol>
							</td>
						</tr>
						<tr>
						    <td colspan="2">
						        <h3 class="tablehead">How to apply in Pradhan Mantri Jeevan Jyoti Beema Yojana</h3>
						    </td>
						</tr>
						<tr>
							<td colspan="2">
							   प्रधानमंत्री जीवन ज्योति बीमा  योजना में आवेदन करने के लिए आवेदकों को निम्नलिखित चरणों का पालन करना होगाः
								<ol>
									<li>
										आवेदक को सबसे पहले प्रधान मंत्री जीवन ज्योति बीमा योजना में आवेदन प्रक्रिया शुरू करने के लिए निकटतम शाखा या डाकघर केंद्र पर जाना होगा।
									</li>
                                    <li>
										उसके बाद, आवेदन पत्र को लेना होगा।
                                    </li>
                                    <li>
										आपको अपने फॉर्म में सटीक जानकारी भरनी चाहिए और सभी विवरणों को ध्यान पूर्वक पढ़े।
                                    </li>
                                    <li>
										अब, इसके बाद आपको सभी आवश्यक दस्तावेजों, जैसे पैन कार्ड, जाति प्रमाण पत्र आदि की प्रतियां संलग्न करनी होंगी।
                                    </li>
                                    <li>
										अब एक बार जब आप उपरोक्त चरणों को पूरा कर लेते हैं, तो आपको अपना आवेदन पत्र किसी बैंक शाखा या डाकघर में जमा करना चाहिए और रसीद प्राप्त करनी करनी होगी जो की आवश्यक है। 
                                    </li>
                                    <li>
										इन आवेदन चरणों का पालन करके आप अपना आवेदन कर सकते हैं और इस योजना का बीमा लाभ प्राप्त कर सकते हैं। 
                                    </li>
								</ol>
							</td>
						</tr>
