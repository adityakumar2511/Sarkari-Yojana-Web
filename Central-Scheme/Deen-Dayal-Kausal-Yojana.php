                                    <li>इस योजना के लाभार्थी गरीब परिवार से सम्बंधित बेरोजगार होना चाहिए।</li>
								</ol>
							इन सभी पात्रताओं के साथ आप इस दीन दयाल उपाध्याय ग्रामीण कौशल्या योजना में सफलतापूर्वक अपना आवेदन कर सकते हैं।
							</td>
						</tr>
						<tr>
						    <td colspan="2">
						        <h3 class="tablehead"> Objective of Deen Dayal Upadhyaya Grameen Kaushalya Yojana</h3>
						    </td>
						</tr>
						<tr>
							<td colspan="2">
							     दीन दयाल उपाध्याय ग्रामीण कौशल योजना के निम्नलिखित उद्देश्य हैं:
								<ol value="1">
									<li>
                                      इस योजना का उद्देश्य यह है कि ग्रामीण क्षेत्रो में जो युवा गरीब और बेरोजगार है उनकी पहचान करना है और उन्हे लाभान्वित करना है।
									</li>
									<li>	
									इस योजना के अन्तर्गत जो युवा रुचि रखते है उन ग्रामीण युवाओं को संगठित करना हैं।
									</li>
									<li>
										इस योजना के उद्देश्य है कि विभिन्न प्रकार के अवसरों पर समुदाय के भीतर युवाओ को जागरूकता का निर्माण करना है।
									</li>
									<li>
									इस योजना के उद्देश्य है कि युवाओ के	योग्यता के आधार पर उनका चयन करना है।
									</li>
									<li>
								    इस योजना के अन्तर्गत युवाओ के बीच में ज्ञान, उद्योग से जुड़े कौशल और दृष्टिकोण प्रदान करना है जो रोजगार क्षमता को बढ़ाते हैं।
									</li>
									<li>
									 इस योजना के अन्तर्गत ग्रामीण युवाओं और उनके माता-पिता की काउंसलिंग करना है।
									</li>
									<li>
									इस योजना का उद्देश्य प्लेसमेंट के बाद युवाओ स्थिरता को स्थिरता प्रदान करना है जिससे उनकी बेरोजगारी दूर हो सके।
									</li>
									<li>
									इस योजना का उद्देश्य युवाओ को ऐसी नौकरियां प्रदान करना है जिससे न्यूनतम मजदूरी से अधिक भुगतान मिल सके।
									</li>
								</ol>
							</td>
						</tr>
