								     <li> आवेदन करने के लिए, आवेदक के पास <b> राशन कार्ड </b>  होना आवश्यक है। </li>  
								     <li>आवेदन करने के लिए, आवेदक के पास <b> शैक्षिक योग्यता प्रमाण पत्र </b> होना आवश्यक है।</li>
								     <li>आवेदन करने के लिए, आवेदक के पास <b> आय प्रमाण पत्र </b>  होना आवश्यक है।</li> 
								     <li>आवेदन करने के लिए, आवेदक के पास <b> पासपोर्ट साइज फोटो </b>  होना आवश्यक है। </li> 
								     <li> आवेदन करने के लिए, आवेदक के पास  ग्रामीण क्षेत्र में<b> निवास का प्रमाण पत्र </b> होना आवश्यक है। </li>
	 							     <li>आवेदन करने के लिए, आवेदक के पास <b> जाति प्रमाण पत्र </b> होना आवश्यक है। </li>
								     <li>आवेदन करने के लिए, आवेदक के पास <b> अधिवास प्रमाणपत्र </b> होना आवश्यक है। </li>
								</ol>
								इन सभी दस्तावेजों के साथ आप इस दीन दयाल उपाध्याय ग्रामीण कौशल्या योजना में सफलतापूर्वक अपना आवेदन कर सकते हैं।
