						        <h3 class="tablehead"> How to apply in Deen Dayal Upadhyaya Grameen Kaushalya Yojana</h3>
						    </td>
						</tr>
						<tr>
							<td colspan="2">
							    दीन दयाल उपाध्याय ग्रामीण कौशल्या योजना में आवेदन करने के लिए आवेदकों को निम्नलिखित चरणों का पालन करना होगाः
								<ol>
									<li>
								     सर्वप्रथम आवेदक को आवेदन करने के लिए Deen Dayal Upadhyaya Grameen Kaushalya की आधिकारिक वेबसाइट पर जाए।
									</li>
									<li>
										अब आवेदक इस योजना मे पंजीकरण विकल्प का चयन करें। उसके बाद आवेदक को एक नए पृष्ठ प्रदर्शित होगा, जिससे बाद आवेदक को नए पेज पर फ्रेश/न्यू रजिस्ट्रेशन का चुनाव करना होगा और Next पर क्लिक करना होगा।
									</li>
									<li>
									अब आवेदक को Deen Dayal Upadhyaya Grameen Kaushalya Yojana पर क्लिक करना होगा जिससे बाद आवेदक को अपनी सामाजिक-आर्थिक और जाति जनगणना, पते की जानकारी, व्यक्तिगत जानकारी सहित सभी जानकारियां ध्यान पूर्वक भरिये और उसके पश्चात् सेव एंड प्रोसीड पर क्लिक करें।
									</li>
									<li>
									अब आवेदक अपने पसंदीदा प्रशिक्षण कार्यक्रम का चयन करें और विवरण को ध्यान पूर्वक भरें।
									</li>
									<li>
								    अब अन्तत: आवेदक को अपनी पूरी भरी गयी जानकारी को पढ़े और पुष्टि करें की आप के द्वारा भरी गयी सभी जानकारी सही भरी गयी हैं और अब अंत में आवेदन जमा करें।
									</li>
								</ol>
								अब आपका आवेदन दीन दयाल उपाध्याय ग्रामीण कौशल्या योजना में सफलतापूर्वक पूरा हो जाएगा।
							</td>
						</tr>
