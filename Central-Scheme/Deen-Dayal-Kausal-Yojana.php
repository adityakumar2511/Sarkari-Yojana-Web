									    इसके बाद आवेदक को लॉग इन के विकल्प पर जाकर क्लिक करना होगा।
									</li>
									<li>
									    अब आवेदक के सामने लॉग इन विकल्प खुल जायेगा।
									</li>
									<li>
									    आवेदक लॉग इन फॉर्म में अपना यूजरनेम पासवर्ड और कैप्चा कोड दर्ज करना होगा।
									</li>
