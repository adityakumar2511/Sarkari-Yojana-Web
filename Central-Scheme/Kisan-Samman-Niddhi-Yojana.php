                                    इसके तद्पश्चात् आपका आधार UIDAI से प्रमाणित हो जाएगा फिर आप पंजीकरण के लिए तभी आगे बढ़ पाएंगे। 
									</li>
									<li>
                                    अब आवेदक अपना व्यक्तिगत विवरण दर्ज करें. 
