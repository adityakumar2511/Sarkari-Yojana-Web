							<td colspan="2">
							    इस प्रधानमंत्री किसान सम्मान निधि योजना के लाभ निम्नलिखित हैंः
								<ol>
                                    <li>
                                    इस योजना के आवेदक को 6,000 प्रति वर्ष रुपये की प्रत्यक्ष आर्थिक सहायता प्रदान की जायेगी।
                                    </li>
                                    <li>
                                    इस योजना के तहत आवेदक को कृषि सहायता, उपकरण, संसाधन प्रदान किये जाते हैं।
                                    </li>
                                    <li>
                                    इस योजना के तहत आवेदक को किस्तों के माध्यम से आर्थिक सहायता प्रदान की जाती हैं।
                                    </li>
								</ol>
								उपर्युक्त लाभ इस प्रधानमंत्री किसान सम्मान निधि योजना के तहत दिए जाएंगे।
							</td>
