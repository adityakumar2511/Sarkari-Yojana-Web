						<tr>
							<td colspan="2">
							     प्रधानमंत्री किसान सम्मान निधि योजना के लिए निम्नलिखित संक्षिप्त जानकारी हैं:
								<ol>
									<li>
                                        पीएम सम्मान निधि योजना के सम्बंध में किसानों के लाभ के लिए एक महत्पूर्ण खबर सामने आई है। यह खबर है कि जो भी किसान इस योजना का लाभ उठा रहे हैं उन्हें जल्द ही अपना ई-केवाईसी करा लेना चाहिए, नहीं तो बिना ई-केवाईसी के किसान इस योजना का लाभ से वंचित हो जायेंगे। पीएम सम्मान निधि योजना के तहत ई-केवाईसी की अंतिम तिथि दिनांक 15 जनवरी 2024 निर्धारित की गई है।
                                    </li>
                                    <li>
                                      सूचना के अनुसार आपको बता दें कि प्रधानमंत्री किसान सम्मान निधि योजना के तहत किसानों को प्रत्येक साल 6 हजार रुपये की आर्थिक सहायता (लाभ) केंद्र सरकार के द्वारा दी जायेगी। ये आर्थिक राशि किसानों के खाते में चार महीने के समय अंतराल में 3 किस्तों में 2-2 हजार रुपये करके भेजी जायेंगी। 
                                    </li>
                                    <li>
                                        पीएम किसान सम्मान निधि योजना में पंजीकृत समस्त किसानों के द्वारा ई-केवाईसी करवाने की अंतिम तिथि 15 जनवरी 2024 निर्धारित की गई है जिस भी किसान ने प्रधानमंत्री किसान सम्मान निधि योजना में ई-केवाईसी और डीबीटी, आधार सीडिंग संबंधी काम नहीं करवाया है, उन सभी के लिए सभी पंचायतों में राजीव गांधी सेवा केंद्र पर 16 दिसंबर 2023 से 12 जनवरी 2024 तक कैंपों का आयोजन किया गया है। जिसमें किसानों के ई-केवाईसी व नए आवेदन का काम सीएससी द्वारा / डीबीटी का काम/ पोस्ट ऑफिस/ बैंक के द्वारा और भूमि सत्यापन का करने का कार्य पटवारी द्वारा किया जा रहा है।
                                    </li>
