                                    इसके तद्पश्चात् आवेदक अपना बैंक खाता विवरण दर्ज करें. 
									</li>
                                    <li>
                                    इसके तद्पश्चात् आवेदक अपनी भूमि के स्वामित्व का विवरण दर्ज करें। 
                                    </li>
                                    <li>
