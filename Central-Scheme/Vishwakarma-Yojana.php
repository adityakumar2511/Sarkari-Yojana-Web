								<ol>
									<li>
                                      इस योजना के तहत लाभ के रूप में आवेदक को पहचान प्रमाण पत्र और पहचान पत्र के माध्यम से विश्वकर्मा के रूप में मान्यता प्राप्त होगी।
                                    </li>
                                    <li>
                                     इस योजना के तहत आवेदक को लाभ के रूप में कौशल सत्यापन के बाद 5-7 दिन (40 hours) बुनियादी प्रशिक्षण दिया जाता हैं।
                                    </li>
                                    <li> 
                                      इस योजना के तहत इच्छुक उम्मीदवार 15 दिनों या (120 hours) उन्नत प्रशिक्षण के लिए भी नामांकन कर सकते हैं। 
                                    </li>
                                    <li>
                                       इस योजना के तहत आवेदक को लाभ के रूप में प्रशिक्षण वजीफाः 500 रुपये प्रतिदिन दिये जाते हैं।
                                    </li>
                                    <li>
                                       इस योजना के तहत आवेदक को लाभ के रूप में टूलकिट प्रोत्साहनः 15,000 रुपये का अनुदान दिया जाता हैं। 
                                    </li>
                                    <li>
                                      इस योजना के तहत आवेदक को लाभ के रूप में  ऋण समर्थन भी दिया जाता हैं।
                                    </li>
                                    <li>
                                       इस योजना के तहत आवेदक को लाभ के रूप में संपार्श्विक मुक्त उद्यम विकास ऋणः 1 लाख रुपये (18 महीने के पुनर्भुगतान के लिए पहली किश्त) और 2 लाख रुपये (Second Tranche for 30 months repayment) दिया जायेगा।
                                    </li>
                                    <li>
                                      इस योजना के तहत आवेदक को लाभ के रूप में  रियायती ब्याज दर पर लाभार्थी से 8% की ब्याज अनुदान सीमा के साथ 5% का शुल्क लिया जाएगा जिसका भुगतान एमओएमएसएमई द्वारा किया जाएगा।
                                    </li>
                                    <li>
                                       इस योजना के तहत आवेदक को लाभ के रूप में ऋण गारंटी शुल्क का वहन भारत सरकार द्वारा किया जाएगा।
                                    </li>
                                    <li>
                                        इस योजना के तहत आवेदक को लाभ के रूप में डिजिटल लेन-देन के लिए प्रोत्साहनः अधिकतम 100 लेनदेन के लिए प्रति लेनदेन 1 रुपये (monthly) दिया जाता हैं।
                                    </li>
                                    <li>
                                        इस योजना के तहत आवेदक को लाभ के रूप में विपणन समर्थन राष्ट्रीय विपणन समिति (एनसीएम) गुणवत्ता प्रमाणन, ब्रांडिंग और संवर्धन, ई-कॉमर्स लिंकेज, व्यापार मेलों के विज्ञापन, प्रचार और अन्य विपणन गतिविधियों जैसी सेवाएं प्रदान करेगी। 
                                    </li>
                                    <li>
                                        इन सभी लाभों के अलावा डिजिटल सशक्तिकरण को बढ़ावा देना के लिए और अवसरों के सहायता हेतु बाजार लिंकेज और ब्रांड प्रचार के लिए एक मंच प्रदान करना हैं प्रधानमंत्री विश्वकर्मा योजना शिल्पकारों और पारंपरिक कारीगारों के कार्यों को बढ़ानें के लिए एक महत्वपूर्ण प्रयास किया गया हैं।
