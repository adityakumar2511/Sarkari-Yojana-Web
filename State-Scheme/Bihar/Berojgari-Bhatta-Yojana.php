						<tr>
							<td colspan="2">
							    बेरोजगारी भत्ता योजना के लिए निम्नलिखित संक्षिप्त जानकारी है:
						        <ol>
						            <li>
						              इस योजना के माध्यम से सभी बिहार के बेरोजगार युवाओं को वित्तीय/आर्थिक सहायता देने का प्रदान किया गया हैं। जिससे उनके जीवन में काफी सुधार आयेगा और वह अपनी परिवार का भरण पोषण कर सकेंगे। 
						            </li>
						            <li>
						                बिहार राज्य के सभी शिक्षित उम्मीदवार जिनकी पढ़ाई सम्पूर्ण हो गयी है और उन्हे अभी तक किसी भी प्रकार की नौकरी नहीं मिल पा रही है। जिससे वह  अभी तक बेरोजगार हैं वह सभी इस बेरोजगारी भत्ता योजना  का लाभ उठा सकेंगे।
						            </li>
						            <li>
						              बिहार राज्य के बेरोजगारों के लिए शिक्षा विभाग, योजना एवं विकास विभाग एवं श्रम संसाधन विभाग के द्वारा जो भी बेरोजगार व्यक्ति 20 से 25 वर्ष के बेरोजगार युवा है उनको रोजगार खोजने के लिए कुल दो वर्षो तक प्रत्येक माह 1000 रुपये इस योजना के तहत दिए जा रहें हैं  |
						            </li>
						            <li>
						                इस योजना का उद्देश्य की पूर्ति के लिए बिहार राज्य सरकार के द्वारा एक नई “मुख्यमंत्री निश्चित स्वयं सहायता भत्ता योजना” प्रारम्भ करने का निर्णय लिया गया है और इस योजना का कार्यान्वित करने का निर्णय और आदेश दिया गया है | इस योजना के तहत कार्यान्वयन का सम्पूर्ण दायित्व  योजना एवं विकास विभाग को दिया गया है | मुख्यमंत्री जी यह निश्चय स्वयं सहायता भत्ता योजना के सफलतापूर्वक कार्यान्वयन हेतु प्रत्येक जिला मुख्यालय के लिए जिला निबंधन एवं परामर्श केंद्र स्थापित करने का आदेश भी दिया गया है|
						            </li>
						            <li>
						                मुख्यमंत्री निश्चय स्वयं सहायता भत्ता योजना के निबंधन केन्द्रो का संचालन 15 सितम्बर 2016 से प्रारम्भ किया गया है और अभी तक औपचारिक रूप से 02 अक्टूबर 2016 से इसका संचालन प्रारम्भ किया जा चुका है |
						            </li>
						        </ol>
