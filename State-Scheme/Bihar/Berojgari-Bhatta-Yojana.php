								    <li>आवेदन करने के लिए, आवेदक के पास <b> मोबाइल नम्बर</b> होना आवश्यक है। </li>
								</ol>
								इन सभी दस्तावेजों के साथ आप सफलतापूर्वक इस बेरोजगारी भत्ता योजना में अपना आवेदन कर सकते हैं।
