								<ol>
                                    <li>
                                       इस योजना के तहत 20 से 25 वर्ष की आयु के 12वीं कक्षा उत्तीर्ण बेरोजगार युवक/युवती को रोजगार तलाशने के दौरान स्वयं सहायता भत्ता की राशि को प्रदान कर आर्थिक मदद कराना है। 
                                    </li>
                                    <li>
                                      इस योजना के तहत युवाओं को स्वयं सहायता भत्ता की राशि का भुगतान योग्य आवेदक को प्रतिमाह 1000 रूपये की दर से अधिकतम दो वर्षों तक दिया जाएगा। 
                                    </li>
                                    <li>
                                        इस योजना के माध्यम से युवाओ को बेरोजगार होते हुए भी आर्थिक तोर पर सहायता प्रदान करना है | 
                                    </li>
                                    <li>
                                        इस योजना का उद्देश्य बिहार के सभी शिक्षित बेरोगार युवाओ को प्रतिमाह 1000 रूपये का बेरोजगारी भत्ता प्रदान करना है ।
                                    </li>
								</ol>
							उपरोक्त उल्लिखित लाभ इस बेरोजगारी भत्ता योजना के तहत दिए जाएंगे।
							</td>
						</tr>
						<tr>
						    <td colspan="2">
						        <h3 class="tablehead">How to apply in Berojgari Bhatta Yojana</h3>
						    </td>
						</tr>
						<tr>
							<td colspan="2">
							    बेरोजगारी भत्ता योजना में आवेदन करने के लिए आवेदकों को निम्नलिखित चरणों का पालन करना होगा:
								<ol>
									<li>
									 इस योजना में आवेदन के लिए आवेदक सर्वप्रथम शिक्षा, विकास और श्रम संसाधन विभाग की आधिकारिक वेबसाइट पर जाएं।
									</li>
									<li>
									इसके तद्पश्चात्	होम पेज पर, 'नया आवेदक पंजीकरण' पर क्लिक करें।
									</li>
                                    <li>
                                       इसके तद्पश्चात् नाम, ईमेल आईडी, आधार नंबर और मोबाइल नंबर की जानकारी को भरकर पंजीकरण फॉर्म को भरें।
                                    </li>
									<li>
									इसके तद्पश्चात्	ओटीपी भेजें और इसे कैप्चा कोड के साथ दर्ज करें और सबमिट बटन पर क्लिक करें।
									</li>
									<li>
									इसके तद्पश्चात्	समस्त जरूरी दस्तावेज अपलोड करें।
									</li>
									<li>
									इसके तद्पश्चात्	उपयोगकर्ता नाम, पासवर्ड और कैप्चा कोड का उपयोग करके वेबसाइट पर लॉग इन करें।
									</li>
                                    <li>
                                      अब आवेदक पंजीकरण प्रक्रिया पूरी करें। 
                                    </li>
								</ol>
								अब आपका आवेदन बेरोजगारी भत्ता योजना में सफलतापूर्वक पूरा हो जायेगा।
							</td>
						</tr>
							<tr>
						    <td colspan="2">
						        <h3 class="tablehead">How to check status in Berojgari Bhatta Yojana</h3>
						    </td>
