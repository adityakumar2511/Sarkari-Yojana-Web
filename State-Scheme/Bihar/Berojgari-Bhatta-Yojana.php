							<td colspan="2">
							    बेरोजगारी भत्ता योजना की स्थिति जांचने के लिए आवेदकों को निम्नलिखित चरणों का पालन करना होगा:
								<ol>
									<li>
								        आवेदक को सर्वप्रथम बेरोजगारी भत्ता योजना की अधिकारिक वेबसाइट पर जाना होगा।
									</li>
									<li>
								    अब आवेदक को अधिकारिक वेबसाइट पर होम पेज पर एप्लीकेशन स्टेटस के विकल्प का चयन करना होगा।
									</li>
                                    <li>
                                     अब इसके तद्पश्चात् आवेदक को अपने स्क्रीन पर एक नया पृष्ठ प्रदर्शित होगा।
                                    </li>
									<li>
								    अब आवेदक को इस पेज पर रजिस्ट्रेशन आई डी और आधार कार्ड में से किसी एक आप्शन का चयन करना आवश्यक होगा।
									</li>
									<li>
								    अब आवेदक जिस विकल्प का चयन किया होगा उसमें अपना नंबर और जन्मतिथि स्थान को ध्यान पूर्वक भरना होगा।
									</li>
									<li>
							          अब आवेदक को इसके कैप्चा कोड दर्ज करना होगा और सबमिट बटन पर क्लिक करना होगा।
									</li>
                                    <li>
                                    अब इन सभी चरणों के बाद आवेदक को अपने स्क्रीन पर आवेदन का स्टेटस प्रदर्शित होगा और आसानी से अपनी जानकारी प्राप्त कर पायेगा।
                                    </li>
								</ol>
