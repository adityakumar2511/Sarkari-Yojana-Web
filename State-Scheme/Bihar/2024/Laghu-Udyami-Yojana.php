						        <h3 class="tablehead">How to apply in Laghu Udyami Yojana</h3>
						    </td>
						</tr>
						<tr>
							<td colspan="2">
							    लघु उद्यमी योजना में आवेदन करने के लिए आवेदकों को निम्नलिखित चरणों का पालन करना होगा:
								<ol>
									<li>
                                        सर्वप्रथम आपको उद्योग विभाग बिहार सरकार की आधिकारिक वेबसाइट पर जाना होगा।
									</li>
									<li>
                                        इसके बाद आपके वेबसाइट पर होम पेज खुल जाएगा।
									</li>
                                    <li>
                                        अब बिहार लघु उद्यमी योजना होम पेज पर आपको पंजीकरण के ऑप्शन पर क्लिक करें।
                                    </li>
									<li>
                                        अब क्लिक करते ही आपके सामने लॉगिन पेज खुलेगा।
									</li>
									<li>
                                        अब बिहार लघु उद्यमी योजना  के इस पेज पर अपनी आधार संख्या और पासवर्ड दर्ज कर लॉगिन करें के विकल्प पर क्लिक करें।
									</li>
									<li>
                                        अब क्लिक के बाद आपके सामने उद्यमी योजना पंजीकरण फॉर्म खुलेगा।
									</li>
                                    <li>
                                        अब इसके बाद आपको इस पेज पर पूछी गई सभी जानकारी को दर्ज करना होगा और सबमिट पर क्लिक करना होगा।
                                    </li>
                                    <li>
                                        अब इसके बाद सभी जरूरी जानकारी और दस्तावेज दर्ज करें।
                                    </li>
                                    <li>
                                        अब अंत में सबमिट के ऑप्शन पर क्लिक करना होगा।
                                    </li>
                                    <li>
                                        इस प्रकार से आप बिहार लघु उद्यमी योजना के अंतर्गत ऑनलाइन आवेदन कर सकते हैं।
                                    </li>
								</ol>
								अब आपका आवेदन लघु उद्यमी योजना में सफलतापूर्वक पूरा हो जायेगा।
							</td>
