							    लघु उद्यमी योजना के लिए निम्नलिखित संक्षिप्त जानकारी है:
						        <ol>
						            <li>
                                        बिहार सरकार के द्वारा राज्य के आर्थिक रूप से कमजोर वर्ग के परिवार के लोगों को उद्यम स्थापित कराने के लिए 16 जनवरी 2024 को बिहार मंत्रिमंडल की बैठक में बिहार लघु उद्यमी योजना को मंजूरी दी गई है। इस योजना के अंतर्गत स्वरोजगार और उद्यमिता के लिए प्रत्येक गरीब परिवार को 2 लाख रुपए की आर्थिक सहायता राशि प्रदान की जाएगी। बिहार राज्य सरकार द्वारा लघु उद्यमी योजना के माध्यम से 94 लाख परिवारों को लाभ दिया जायेगा। 
						            </li>
						            <li>
                                        बिहार लघु उद्यमी योजना के अंतर्गत 2 लाख रुपए की वित्तीय सहायता का लाभ देने के लिए गरीब परिवारों को ऑनलाइन आवेदन करना होगा और इसके बाद आवेदक का रेंडम चयन किया जाएगा। बिहर राज्य सरकार द्वारा सत्र 2024-25 के लिए 1000 करोड़ रुपए का बजट निर्धारित किया गया है जो सरकार द्वारा प्रदान की जाने वाली वित्तीय सहायता राशि सीधे लाभार्थियों के बैंक खाते में भेजी जाएगी। इस योजना का लाभ प्राप्त करके गरीब नागरिक स्वतः ही अपना व्यापार शुरू करके आत्मनिर्भर हो सकता है। जिससे भविष्य में बिहार राज्य में भी बेरोजगारी की समस्या कम हो सकेगी। 
						            </li>
