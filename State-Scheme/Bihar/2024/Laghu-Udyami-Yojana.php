						    <td colspan="2">
						        <h3 class="tablehead"> Benefits of Laghu Udyami Yojana</h3>
						    </td>
						</tr>
                        <tr>
							<td colspan="2">
							    इस लघु उद्यमी योजना के लाभ निम्नलिखित हैं:
								<ol>
                                    <li>
                                        बिहार राज्य के मुख्यमंत्री नीतीश कुमार की अध्यक्षता में राज्य के कमजोर वर्गों के गरीब परिवारों को आर्थिक विकास के लिए और खुदका उद्यम स्थापित करने के लिए बिहार लघु उद्यमी योजना को शुरू की गयी है।
                                    </li>
                                    <li>
                                        इस योजना के अंतर्गत गरीब परिवारों को स्वरोजगार स्थापित करने के लिए दो-दो लाख रुपए की वित्तीय सहायता राशि का लाभ दी जा रही है।
                                    </li>
                                    <li>
                                        बिहार राज्य सरकार द्वारा बिहार लघु उद्यमी योजना के अंतर्गत यह राशि तीन किस्तों में  दी जा रही है।
                                    </li>
                                    <li>
                                        यह वित्तीय सहायता राशि लाभार्थियों के सीधे बैंक खाते में भेजी जा रही है।
                                    </li>
                                    <li>
                                        इस योजना को शुरू करने के लिए बिहार सरकार द्वारा 1250 करोड़ रुपए का बजट निर्धारित किया है।
                                    </li>
                                    <li>
                                        यह योजना अगले 5 सालों तक बिहार राज्य में लागू रहेगी।
                                    </li>
                                    <li>
                                        बिहार राज्य के 94 लाख से अधिक गरीब परिवारों के कम से कम एक सदस्य को स्वरोजगार के लिए आर्थिक सहायता प्रदान किया जायेगा।
                                    </li>
                                    <li>
                                        वित्तीय सहायता प्रदान करने के लिए स्वरोजगार स्थापित करने के लिए 62 उद्योग को बिहार लघु उद्यमी योजना के तहत शामिल किया गया है।
                                    </li>
                                    <li>
                                        इस योजना का देखरेख और संचालन बिहार उद्योग विभाग के द्वारा किया जाएगा।
                                    </li>
                                    <li>
                                       बिहार लघु उद्यमी योजना के तहत ऑनलाइन प्रक्रिया के माध्यम से ही आवेदन स्वीकार किए जा रहर हैं।
                                    </li>
                                    <li>
                                        यह योजना केवल गरीब परिवारों को वित्तीय सहायता का लाभ प्रदान कराने के लिए संचालित की गयी है।
                                    </li>
                                    <li>
                                        बिहार लघु उद्योग योजना के जरिये आर्थिक रूप से कमजोर वर्ग के नागरिक बिना किसी परेशानी के खुद का रोजगार स्थापित कर सकेंगे।
                                    </li>
                                    <li>
                                        इस योजना को सभी वर्गों के लोगों को बिना किसी भेदभाव के सहायता प्रदान कर आर्थिक स्थिति में सुधार लाएगी। 
                                    </li>
								</ol>
							उपरोक्त उल्लिखित लाभ इस लघु उद्यमी योजना के तहत दिए जाएंगे।
							</td>
						</tr>
