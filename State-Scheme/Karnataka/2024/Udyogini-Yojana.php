                                    <li>
                                        महिलाओं को अपनी कंपनियां और सूक्ष्म उद्यम शुरू करके आत्मनिर्भर बनने में सहायता करना है और उन्हें इसके लिए साहूकारों से अत्यधिक ब्याज दरों पर पैसा उधार लेने से रोकना भी एक उद्देश्य है। वित्तीय मदद के साथ-साथ कौशल विकास पाठ्यक्रम भी उपलब्ध कराने की योजना बनायी गयी है। 
                                    </li>
                                    <li>
                                        महिला उधारकर्ताओं को बैंकों और अन्य वित्तीय संगठनों से ऋण लेने के लिए प्रोत्साहित करना है ताकि वे पैसा कमाना शुरू कर सकें और सशक्त बन सकें। इस योजना में पात्र महिला लाभार्थियों को बिना किसी पूर्वाग्रह या भेदभाव के ब्याज मुक्त ऋण प्रदान करना एससी/एसटी या अन्य विशेष श्रेणियों के तहत आने वाली महिलाओं के लिए वित्तीय सहायता को और अधिक किफायती बनाया जायेगा। 
                                    </li>
                                    <li>
                                        महिलाओं को निजी ऋणदाताओं या अन्य वित्तीय संगठनों से उच्च-ब्याज ऋण प्राप्त करने से रोकेंगी ईडीपी प्रशिक्षण के माध्यम से, सुनिश्चित भी किया जायेगा कि लाभार्थी महिलाएं अपने कौशल में वृद्धि करें। 
                                    </li>
