                                    </li>
                                    <li>
                                        अब इसके बाद पॉप-अप बॉक्स में दिए गए लिंक पर क्लिक करना होगा। 
                                    </li>
                                    <li>
                                        अब योजना के लिए मांगे गए सभी आवश्यक जानकारी भरें और दस्तावेज अपलोड करना होगा। 
                                    </li>
                                    <li>
                                        और अब अंत में सबमिट बटन पर क्लिक करें और भविष्य के संदर्भ के लिए आवेदन पत्र संख्या नोट कर लें। 
                                    </li>
								</ol>
