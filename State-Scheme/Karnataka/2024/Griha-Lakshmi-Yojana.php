                                    </li>
                                    <li>
										गृह  लक्ष्मी योजना के द्वारा महिलाओं की सहायता और उनकी आजीविका में सुधार के लिए शुरूआत की गई है। इसका उद्देश्य गृहिणियों, भूमिहीन महिलाओं और कृषि महिला श्रमिकों को प्रत्यक्ष लाभ हस्तांतरण मोड के माध्यम से वित्तीय सहायता प्रदान करके उन्हें सशक्त बनाना है। मुख्यमंत्री सिद्धारमैया को 1गयी है जो सीधे लाभार्थियों के बैंक खातों में स्थानांतरित कर दी जा रही है! 
                                    </li>
                                    <li>
                                        <b>कर्नाटक में गृह लक्ष्मी योजना राशि:-</b> कर्नाटक सरकार ने पात्र महिलाओं को हर महीने 2,000 रुपये की वित्तीय सहायता प्रदान करा रही है । सरकार ने 30 अगस्त 2023 से 2,000 रुपये की राशि डीबीटी के माध्यम से वितरित की है। 
                                    </li>
