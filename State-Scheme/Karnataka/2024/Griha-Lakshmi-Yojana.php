                               <br>
                               गृह लक्ष्मी योजना के लिए पंजीकरण 19 जुलाई 2023 से शुरू किया गया है । पात्र महिलाएं नीचे दिए गए सभी प्रक्रिया का पालन करके योजना के लिए ऑनलाइन पंजीकरण कर सकती हैं: 
								<ol>
									<li>
                                        सर्वप्रथम इस योजना के आवेदन के लिए आवेदिका को सेवा सिंधु गारंटी योजना पोर्टल पर जाएं । 
									</li>
                                    <li>
