                                        </li>
                                        <li>
                                            <b>तीसरी किस्त:-</b> इस योजना की तीसरी किस्त में 4,000 रुपए की सहायता राशि दी जाती है जो किसी भी राजकीय विद्यालय में  प्रथम कक्षा में प्रवेश लेने पर दी जाती है
                                        </li>
                                        <li>
                                            <b>चौथी किस्त:-</b> इस योजना में चौथी किस्त में 5,000 रुपए की सहायता राशि दी जाती है जिसे किसी भी राजकीय विद्यालय में कक्षा 6 में प्रवेश लेने पर मिलता है।
                                        </li>
                                        <li>
                                            <b>पाँचवी किस्त:-</b> इस योजना में बेटी जब राजकीय स्कूल की 10वीं कक्षा में प्रवेश लेती है तब उसे पांचवी किस्त के रूप में 11,000 रुपए की राशि दी जाती है।
                                        </li>
                                        <li>
                                            <b>छठी किस्त:-</b> इस योजना की छठी किस्त के रूप में 25,000 रुपए की सहायता राशि प्रदान की जाती है। जिसे बालिका के राजकीय विद्यालय के 12वीं कक्षा में प्रवेश लेने पर दी जाती है।
                                        </li>
                                   </ul>
