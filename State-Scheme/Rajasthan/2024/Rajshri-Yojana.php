                                           <b>पहली किस्त:-</b> इस योजना की पहली किस्त बालिका के जन्म पर दी जाती है। जिसमें सहायता राशि 2500 रुपए की दी जाती है। यह राशि जननी सुरक्षा योजना के तहत देय राशि के अतिरिक्त आती है।
                                        </li>
