                                    इस योजना के तहत जो केंद्र या राज्य सरकार की किसी योजना के अंतर्गत छात्रवृत्ति, सहायता या लाभ प्राप्त कर रहे हैं वह इस योजना का लाभ प्राप्त करने के पात्र नहीं है।
                                    </li>
                                    <li>
                                    इस योजना के तहत जो सरकारी या निजी क्षेत्र में कार्यरत हैं या जिनके स्वयं का रोजगार है वह भी इस योजना का लाभ प्राप्त करने के पात्र नहीं है।
                                    </li>
                                    <li>
                                    इस योजना के तहत वह नागरिक जिनके विरुद्ध आपराधिक प्रकरण दर्ज हो वह भी इस योजना का लाभ नहीं प्राप्त कर सकते हैं। 
                                    </li>
                                    <li>
                                    इस योजना के तहत वह सभी बेरोजगार नागरिक जिन्हेंने स्नातक तक शिक्षा प्राप्त की है एवं उनकी पारिवारिक आय 2 लाख से अधिक है वह व्यक्ति इस योजना का लाभ नहीं प्राप्त कर सकते।
                                    </li> 
                                    <li>
                                   इस योजना के तहत  वह नागरिक जो किसी अन्य योजना जैसे कि मनरेगा के तहत लाभ प्राप्त कर रहे हैं वह व्यक्ति इस योजना का लाभ प्राप्त नहीं कर सकते हैं। 
                                    </li>
								</ol>
								इन सभी बिंदुओं के अनुसार, व्यक्ति इस युवा संबल योजना के लिए पात्र नहीं है।
