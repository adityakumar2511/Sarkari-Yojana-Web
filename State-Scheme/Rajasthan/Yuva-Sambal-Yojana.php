							   युवा संबल योजना के लिए निम्नलिखित पात्रता आवश्यक है:
								<ol>
									<li>
                                    इस योजना में आवेदन के लिए आवेदक को राजस्थान का स्थाई निवासी होना अनिवार्य है।
                                     </li>
                                    <li>
                                    इस योजना का लाभ वहीं महिलाएं उठा सकती हैं जिनका विवाह राजस्थान के किसी स्थाई निवासी पुरुष से हुआ है।
                                    </li>
                                    <li>
                                    इस योजना का लाभ उठाने के लिए सामान्य वर्ग के व्यक्ति की आयु 21 से 30 वर्ष के बीच होनी चाहिए।
                                    </li>
                                    <li>
                                    इस योजना के तहत SC/ST वर्ग के व्यक्ति की आयु 21 से 35 वर्ष के बीच होनी चाहिए।
                                    </li>
                                    <li>
                                    इस योजना का लाभ उठाने के लिए आवेदक के परिवार की वार्षिक आय दो लाख से अधिक नहीं रहना चाहिए।
                                    </li>
                                    <li>
                                    इस योजना के तहत एक परिवार के केवल दो ही व्यक्ति इस योजना का लाभ उठा सकते हैं।
                                    </li>
                                    <li>
                                    इस योजना के तहत आवेदक व्यक्ति जिन्होंने स्नातक डिग्री पाठ्यक्रम पूरा किया है या फिर अभी पढ़ाई चल रही है। वह भी इस योजना का लाभ उठा सकते हैं।
                                    </li>
                                    <li>
                                    इस योजना के तहत इच्छुक आवेदक किसी सरकारी या निजी क्षेत्र में कार्यरत नहीं होना चाहिए तथा आवेदक के पास स्वरोजगार भी नहीं होना चाहिए।
                                    </li>
								</ol>
								इन सभी पात्रताओं के साथ आप इस युवा संबल योजना में सफलतापूर्वक अपना आवेदन कर सकते हैं।
							</td>
