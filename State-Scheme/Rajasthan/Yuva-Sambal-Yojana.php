								    <li>आवेदन करने के लिए, आवेदक के पास <b> निवास प्रमाण पत्र</b> होना आवश्यक है। </li>
								    <li>आवेदन करने के लिए, आवेदक के पास <b> जाति प्रमाण पत्र</b> होना आवश्यक है। </li>
								   <li>आवेदन करने के लिए, आवेदक के पास <b> बैंक पासबुक</b> होना आवश्यक है। </li>
								</ol>
