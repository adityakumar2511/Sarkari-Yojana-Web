                        <tr>
						    <td colspan="2">
						        <h3 class="tablehead">Benefits of Mukhyamantri Yuva Sambal Yojana</h3>
						    </td>
						</tr>
						<tr>
							<td colspan="2">
							    इस युवा संबल योजना के लाभ निम्नलिखित हैं:
								<ol>
									<li>
                                    राजस्थान मुख्यमंत्री युवा संबल योजना के तहत राजस्थान सरकार के द्वारा सभी शिक्षित बेरोजगार नागरिकों को बेरोजगारी आर्थिक भत्ता प्रदान किया जाएगा। 
                                    </li>
                                    <li>
                                    इस योजना के तहत बेरोजगारी भत्ता राजस्थान के पुरुषों के लिए ₹3000 तथा महिलाओं और ट्रांसजेंडर के लिए ₹3500 का दिए जायेंगे।
                                    </li>
                                    <li>
                                    मुख्यमंत्री युवा संबल योजना के माध्यम से राजस्थान के बेरोजगार नागरिक आर्थिक रूप से सशक्त बनाना है।
                                    </li>
                                    <li>
                                    इस योजना के तहत राजस्थान प्रदेश के नागरिकों को अपनी जरूरतों के लिए अब इस योजना के अंतर्गत किसी पर निर्भर रहने की आवश्यकता नहीं पड़ेगी। 
                                    </li>
                                    <li>
                                    इस योजना के तहत यह बेरोजगार भत्ता केवल 2 साल की अवधि के लिए प्रदान किया जाएगा और यदि किसी नागरिक की 2 साल समाप्त होने से पहले नौकरी लग जाती है तो उसे बेरोजगारी भत्ता नहीं प्रदान किया जाएगा।
                                    </li>
                                    <li>
                                    इस योजना के अंतर्गत आवेदन करने के लिए आवेदक को एंप्लॉयमेंट एक्सचेंज में रजिस्टर्ड करना अनिवार्य है।
                                    </li>
                                    <li>
                                    इस योजना का लाभ राजस्थान के रहने वाला प्रत्येक बेरोजगार नागरिक उठा सकता है।
                                    </li>
								</ol>
								इस युवा संबल योजना के तहत उपरोक्त लाभ दिए जाएंगे।
							</td>
						</tr>
						<tr>
						    <td colspan="2">
						        <h3 class="tablehead">How to apply in Mukhyamantri Yuva Sambal Yojana</h3>
						    </td>
						</tr>
						<tr>
							<td colspan="2">
							    युवा संबल योजना में आवेदन करने के लिए आवेदकों को निम्नलिखित चरणों का पालन करना होगा:
								<ol>
									<li>
                                    इस योजना में आवेदन के लिए आवेदक को सर्वप्रथम सबसे पहले डिपार्टमेंट ऑफ स्किल, एंप्लॉयमेंट एंड एंटरप्रेन्योरशिप राजस्थान की आधिकारिक वेबसाइट पर जाए।
									</li>
									<li>
                                        अब आवेदक के सामने मुख्यमंत्री युवा संबल योजना का होम पेज खुल जायेगा।
									</li>
									<li>
                                    इसके तद्पश्चात् होम पेज मैंन्यू पर क्लिक करें।
									</li>
									<li>
                                    इसके तद्पश्चात् आपको जॉब सीकर ऑप्शन पर क्लिक करें।
									</li>
                                    <li>
                                    अब आवेदक अप्लाई फॉर अनइंप्लॉयमेंट एलाउंस के लिंक पर क्लिक करके आगे बढ़े। 
                                    </li>
                                    <li>
                                    अब आववेदक के सामने एक नया पेज खुल जायेगा।
                                    </li>
                                    <li>
                                    इसके पश्चात् रजिस्ट्रेशन के लिंक पर क्लिक करें। 
                                    </li>
                                    <li>
                                    इसके तद्पश्चात् आवेदक को अपनी कैटेगरी चुने (सिटीजन, उद्योग या फिर गवर्नमेंट एंप्लॉय) के लिंक पर क्लिक करें।
                                    </li>
                                    <li>
                                    अब आवेदक के सामने रजिस्ट्रेशन फॉर्म खुल जायेगा।
                                    </li>
                                    <li>
                                    अब आवेदक रजिस्ट्रेशन फॉर्म में पूछे गए सभी जानकारी ध्यान पूर्वक भरकर सबमिट करें।
                                    </li>
                                    <li>
                                    इसके तद्पश्चात् लॉगइन पेज पर जाकर एसएसओ आईडी, पासवर्ड तथा कैप्चा कोड दर्ज करें।
                                    </li>
                                    <li>
                                    अब आवेदक के सामने रजिस्ट्रेशन फॉर्म खुलेगा जिसमें रजिस्ट्रेशन फॉर्म में पूछी गई सभी जानकारी ध्यान पूर्वक बनी होंगी और सभी महत्वपूर्ण दस्तावेजों को अटैच करें।
                                    </li>
                                    <li>
                                    अन्त:में आवेदक भरी गयी सभी जानकारियाँ का मिलान करके सबमिट के बटन पर क्लिक करें।
                                    </li>
                                    <li>
                                    अब आपका आवेदन प्रक्रिया सफलतापूर्वक सबमिट हो गया है।
                                    </li>
                                </ol>
                                अब आपका आवेदन युवा संबल योजना में सफलतापूर्वक पूरा हो जायेगा।
							</td>
