						<tr>
							<td colspan="2">
							मंहतारी बंदन योजना  की  दूसरी किस्त  के  ₹ 1,000 रुपयो  के  पेमेंट का स्टेट्स चेक करना चाहती हैं वह सभी महिलाएं कुछ स्टेप्स को फॉलो करके  अपना स्टेट्स  चेक कर सकती है जो कि इस प्रकार से बताया गया है – 
								<ol>
									<li>
										योजना की किस्त चेक करने केे लिए सबसे पहले आपको इसके ऑफिसियल वेबसाइट  के होम  पेज पर जाना होगा।
                                    </li>
									<li>
										सबसे पहले होम पेज पर जाने के बाद आपको "आवेदन की स्थिति" का विकल्प मिलेगा जिस पर आपको क्लिक करना होगा, अब क्लिक करने के बाद आपके सामने  स्टेट्स पेज खुलेगा।
                                    </li>
									<li>
										अब यहां आपसे मांगी जाने वाले सभी जानकारीयों को सही से दर्ज करना होगा, अब इसके बाद आपको  सबमिट  के  ऑप्शन  पर क्लिक करना होगा जिसके बाद आपका  स्टेट्स  दिखाई देगा। 
                                    </li>
									<li>
										अब आपको इस  स्टेट्स पेज  के नीचे जाना होगा जहां पर आपको  आपके पेमेंट  का  स्टेट्स  दिखा दिखेगा ।
                                    </li>
									<li>
										अब अन्त मे, इस प्रकार आप आसानी  से  पेमेंट  का  स्टेट्स  घर बैठे ही बहुत सी आसानी से चेक कर सकते हैं और इसका  उपयोग कर सकते है।
                                    </li>
									यह सभी स्टेप्स को फॉलो करके आप बहुत ही आसानी से  महतारी वंदन योजना की दूसरी किस्त महतारी वंदन योजना की दूसरी किस्त  का स्टेट्स चेक  कर सकते है और इसका  पूरा   लाभ लें सकते हैं।
								</ol>
							</td>
						</tr>
						
