                                    <li>
                                        मध्य प्रदेश सरकार के द्वारा शुरू की गई इस योजना के तहत से राज्य के सभी शिक्षित बेरोजगारों को रोजगार प्रशिक्षण दिया जा रहा है।
                                    </li>
                                    <li>
                                         राज्य सरकार द्वारा इस योजना के तहत बेरोजगारो को प्रशिक्षण देकर उन्हें रोजगार तलाशने ने यह योजना सहायता प्रदान करेगी ।
                                    </li>
                                    <li>
                                       इस योजना के तहत मध्य प्रदेश सरकार के द्वारा अपने राज्य के स्नातक कक्षा को पास करने वाले बेरोजगार युवा को प्रशिक्षण देने का लक्ष्य रखा गया है।
                                    </li>
                                    <li>
                                       इस योजना के तहत राज्य सरकार के द्वारा बेरोजगार व्यक्ति को रोजगार प्रशिक्षण देकर उन्हें नौकरी प्रदान कराना है। 
                                    </li>
                                    <li>
                                       मध्य प्रदेश सरकार के द्वारा अपने राज्य के जो बेरोजगार व्यक्ति हैं और वह रोजगार की तलाश कर रहें हैं एवं उन सभी बेरोजगारो को इस योजना में लाभ लिया जा रहा है। 
                                    </li>
                                    <li>
                                        इस योजना के तहत मध्य प्रदेश सरकार के द्वारा बेरोजगारी को देश से हटाने का उद्देश्य भी सुनिश्चित किया गया है।
                                    </li>
								</ol>
