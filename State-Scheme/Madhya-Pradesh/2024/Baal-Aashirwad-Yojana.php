							    इस मुख्यमंत्री बाल आशीर्वाद योजना के लाभ निम्नलिखित हैंः
                                <h5 style="text-align:left;">
                                   &nbsp;&nbsp; आफ्टर केयर तहत आने वाला बच्‍चों को होने वाले लाभ:
                                </h5>
								<ol>
									<li>
                                        आफ्टर केयर में आने वाले आवेदक बालकोंं की योग्यता के अनुसार औद्योगिक संस्थान/ प्रतिष्ठान/ प्रतिष्ठित संस्थाओं में इंटर्नशिप प्राप्‍त करवाकर उसी संस्था में यथासंभव रोजगार दिलाया जायेगा। इंटर्नशिप अवधि के समय 5,000 रूपये प्रतिमाह की आर्थिक सहायता दी जायेगी यह सहायता राशि एक वर्ष की अवधि के लिए रहेगी।
									</li>
									<li>	
                                        आफ्टर केयर को प्रधानमंत्री/मुख्यमंत्री कौशल विकास आदि के अंतर्गत दी जाने वाली शासकीय संस्थाओं में व्यावसायिक प्रशिक्षण भी दिया जायेगा। इसके अंतर्गत पोलीटेकनिक डिप्लोमा, आईटीआई, पैरामेडिकल पाठ्यक्रमों, नर्सिंग, होटल मैनेजमेंट, टूरिज्म, आदि में संबंधित विभाग के द्वारा निःशुल्क व्‍यावसायिक प्रशिक्षण भी दिया जायेगा। व्यावसायिक प्रशिक्षण अवधि के समय 5,000 रूपये प्रतिमाह की आर्थिक सहायता दी जाएगी यह सहायता राशि दो वर्ष अवधि के लिए रहेगी।
									</li>
                                    <li>
                                        इस योजना के अंतर्गत तकनीकी शिक्षा, चिकित्सा शिक्षा एवं आयुष शिक्षा एवं विधि शिक्षा प्रदान की जायेगी और इसमें किसी शासकीय/अशासकीय संस्थाओं में प्रवेश करने वाले केयर लीवर्स को अध्ययन अवधि के दौरान 5,000 रूपये से 8,000 रूपये तक की आर्थिक सहायता राशि प्रतिमाह दी जायेगी। 
                                    </li>
								</ol>
                                <h5 style="text-align:left;">
                                    &nbsp;&nbsp;स्पॉन्सरशिप तहत आने वाला बच्‍चों को दिए जाने वाले लाभ: 
                                </h5>
                                <ol>
                                    <li>
                                        इस योजना के अंतर्गत सभी बच्चे को 4000/- प्रतिमाह रुपये दिये जाएंंगे। यह सहयता उसे डी जा रही है जो बच्चे रिश्तेदार अथवा संरक्षक के साथ रहते हैं। यह राशि भी उनको रिश्तेदार अथवा संरक्षक के संयुक्त खाते में जमा किए जायेंगे।
                                    </li>
                                    <li>
                                        स्‍पॉन्‍सरशिप के तहत आने वाले सभी बच्‍चों को चिकित्सा सहायता प्रदान की जायेगी । प्रत्येक बच्चे का भी आयुष्मान कार्ड स्वास्थ्य विभाग द्वारा बनाया जायेगा। 
                                    </li>
                                </ol>
							उपर्युक्त लाभ इस मुख्यमंत्री बाल आशीर्वाद योजना के तहत दिए जाएंगे।
							</td>
