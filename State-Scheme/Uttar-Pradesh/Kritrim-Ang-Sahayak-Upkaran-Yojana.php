								<ol>
                                    <li>
                                       इस योजना के तहत बहु-विकलांग या विकलांग व्यक्तियों के मामले में, जिन्हें एक से अधिक सहायता/सहायता उपकरणों की आवश्यकता होती है, उन्हें एक बार में अधिकतम Rs.10,000/- वित्तीय अनुदान प्रदान किया जाएगा।
                                    </li>  
                                    <li>
                                     इस योजना के तहत विकलांग व्यक्तियों के लिए कृत्रिम अंगों और सहायक उपकरणों की खरीद के लिए वित्तीय अनुदान की अधिकतम राशि Rs.8,000/- होगी। 
                                    </li>
								</ol>
								उपरोक्त उल्लिखित लाभ इस कृत्रिम अंग सहायक उपकरण योजना के तहत दिए जाएंगे।
