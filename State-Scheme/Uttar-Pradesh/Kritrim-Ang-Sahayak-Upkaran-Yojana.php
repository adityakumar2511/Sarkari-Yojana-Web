								<ol>
									<li>
									इस योजना में आवेदन करने के लिए	विकलांग व्यक्ति पीडब्ल्यूडी सशक्तिकरण अधिकारी के कार्यालय/जन सुविधा केंद्र/लोकवाणी केंद्र के माध्यम से आवेदन कर सकते हैं।
									</li>
									<li>
									    इस योजना में आवेदन के लिए आवेदकों को पीडब्ल्यूडी सशक्तिकरण अधिकारी/जन सुविधा केंद्र/लोकवाणी के कार्यालय में जाना होगा।
									</li>
									<li>
										इसके तदपश्चात् आवेदन पत्र प्राप्त करें या इसे यहाँ से डाउनलोड करें। निर्धारित तरीके से फॉर्म भरें।
									</li>
									<li>
									 इसके तद्पश्चात् आवश्यक दस्तावेज (फोटो, निवास/अधिवास प्रमाण पत्र, विकलांगता प्रमाण पत्र, जाति प्रमाण पत्र (यदि अनुसूचित जाति/पिछड़ा वर्ग से संबंधित है) आय प्रमाण पत्र और चिकित्सा रिपोर्ट) संलग्न करें।
									</li>
									<li>
									अब आवेदक आवेदन पत्र को संबंधित अधिकारी को जमा करें।
									</li>
                                    <li>
                                       इस योजना के तहत जिला पीडब्ल्यूडी सशक्तिकरण अधिकारी प्राप्त आवेदनों को वर्गीकृत करके 'पहले आओ और पहले सेवा' के आधार पर उपलब्ध निधि के खिलाफ आवेदकों को वित्तीय अनुदान की मंजूरी प्रदान करेंगे। 
                                    </li>
                                    <li>
                                        इस योजना के तहत पहले आओ पहले पाओ के आधार पर विभिन्न जिलों में शिविरों के माध्यम से लाभार्थियों को सहायक उपकरण वितरित किए जाएंगे।
