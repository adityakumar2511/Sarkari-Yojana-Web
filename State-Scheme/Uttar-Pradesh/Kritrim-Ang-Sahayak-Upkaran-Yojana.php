							<td colspan="2">
							    कृत्रिम अंग सहायक उपकरण योजना के लिए निम्नलिखित संक्षिप्त जानकारी है:
						        <ol>
						            <li>
						                उत्तर प्रदेश सरकार के द्वारा कृत्रिम अंग / सहायक उपकरण योजना  को शुरू किया गया है। इस योजना का उद्देश्य विकलांग व्यक्तियों को वित्तीय आर्थिक अनुदान प्रदान कराना है (जिनकी वार्षिक आय बीपीएल सीमा से अधिक नहीं है, शहरी क्षेत्रों के लिए 56,460/- रूपये और ग्रामीण क्षेत्रों के लिए 46,080/- रूपये सुनिश्चित किया गया है।
						            </li>
						            <li>
                                    <b>प्रमाण पत्र  बनवाने के लिए संपर्क करें:</b>
                                    आवेदक को अनुदान प्राप्त करने के लिए इनमे से किसी एक पदाधिकारी से अपना अनुदान प्रमाण पत्र बनवा सकते हैं :
                                    माननीय सांसद, विधायक,जिला के प्रथम श्रेणी मजिस्ट्रेट, तहसीलदार, महापौर,नगर पंचायत अध्यक्ष, पार्षद, जिला पंचायत अध्यक्ष, बीडीओ या ग्राम प्रधान
                                    इनमे से किसी एक के द्वारा जारी आय प्रमाण पत्र पात्र होंगे।
						            </li>
						        </ol>
							</td>
