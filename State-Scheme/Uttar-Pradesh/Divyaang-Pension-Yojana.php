                                    </li>
                                    <li>
                                       उत्तर प्रदेश विकलांग पेंशन योजना के अंतर्गत उत्तर प्रदेश राज्य सरकार ने सभी विकलांग / दिव्यांग व्यक्तियों के लिए आर्थिक रूप से मजबूत करने के लिए आजीविका के रूप में मदद प्रदान प्रतिमाह 500 रूपये सरकार के द्वारा वित्तीय सहायता दी जा रही है । जिससे इस योजना के माध्यम से सभी दिव्यांग व्यक्ति को अपनी आर्थिक ज़रूरतों को पूरा कर सकते हैं। उत्तर प्रदेश के विकलांग पेंशन योजना के माध्यम से  दिव्यांग व्यक्तियों को आत्मनिर्भर और सशक्त बनाने का लक्ष्य रखा गया है जिससे वह किसी पर बोझ न बन सके और अपना जीवन यापन कर सके।
